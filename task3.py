# Задайте список из n чисел последовательности $(1+\frac 1 n)^n$ и выведите на экран их сумму.


n = int(input('введите число: '))
list= []
for i in range(1, n+1):
    m = i * 3 + 1
    list.append(m)
list2= []
for i in range(1, n+1):
    list2.append(i)
for i in range(0, n):
    print(f'{list2[i]}: {list[i]}', end=', ')
